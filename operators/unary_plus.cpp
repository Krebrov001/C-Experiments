#include <iostream>
using std::cout;
using std::endl;

int main()
{
  int pos_five = 5;
  int neg_five = -5;

  // Unary + operator applied to both variables.
  cout << "+pos_five : " << +pos_five << endl;
  cout << "+neg_five : " << +neg_five << endl;

  // Unary - operator applied to both variables.
  cout << "-pos_five : " << -pos_five << endl;
  cout << "-neg_five : " << -neg_five << endl;

  return 0;
}
